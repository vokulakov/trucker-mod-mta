addEventHandler("onClientResourceStart", resourceRoot, 
	function()
		for _, data in pairs(ModelList) do
			if data.txd then
				local txd = engineLoadTXD(data.txd)
				if type(data.model) == 'table' then
					for _, model in pairs(data.model) do
						engineImportTXD(txd, model)
					end
				else
					engineImportTXD(txd, data.model)
				end
			end
			if data.dff then
				local dff = engineLoadDFF(data.dff)
				engineReplaceModel(dff, data.model)
			end
			if data.col then
				local dff = engineLoadCOL(data.col)
				engineReplaceCOL(dff, data.model)
			end
		end
	end
)
